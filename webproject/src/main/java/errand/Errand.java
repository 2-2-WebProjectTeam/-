package errand;

public class Errand {
	private int errandID;
	private String enrollID;
	private String enrollDate;
	private String errandTopic;
	private String errandDeadLine;
	private String errandPlace;
	private String errandFee;
	private String chattingLink;
	private String errandType;
	private String errandContent;
	private String appliedID;	
	private int errandAvailable;
	
	public int getErrandID() {
		return errandID;
	}
	public void setErrandID(int errandID) {
		this.errandID = errandID;
	}

	public String getEnrollID() {
		return enrollID;
	}
	public void setEnrollID(String enrollID) {
		this.enrollID = enrollID;
	}
	public String getEnrollDate() {
		return enrollDate;
	}
	public void setEnrollDate(String enrollDate) {
		this.enrollDate = enrollDate;
	}

	public String getErrandTopic() {
		return errandTopic;
	}
	public void setErrandTopic(String errandTopic) {
		this.errandTopic = errandTopic;
	}
	public String getErrandDeadLine() {
		return errandDeadLine;
	}
	public void setErrandDeadLine(String errandDeadLine) {
		this.errandDeadLine = errandDeadLine;
	}
	public String getErrandPlace() {
		return errandPlace;
	}
	public void setErrandPlace(String errandPlace) {
		this.errandPlace = errandPlace;
	}
	public String getErrandFee() {
		return errandFee;
	}
	public void setErrandFee(String errandFee) {
		this.errandFee = errandFee;
	}
	
	public String getChattingLink() {
		return chattingLink;
	}
	public void setChattingLink(String chattingLink) {
		this.chattingLink = chattingLink;
	}
	public String getErrandType() {
		return errandType;
	}
	public void setErrandType(String errandType) {
		this.errandType = errandType;
	}
	public String getErrandContent() {
		return errandContent;
	}
	public void setErrandContent(String errandContent) {
		this.errandContent = errandContent;
	}
	public String getAppliedID() {
		return appliedID;
	}
	public void setAppliedID(String appliedID) {
		this.appliedID = appliedID;
	}
	public int getErrandAvailable() {
		return errandAvailable;
	}
	public void setErrandAvailable(int errandAvailable) {
		this.errandAvailable = errandAvailable;
	}
	
}
